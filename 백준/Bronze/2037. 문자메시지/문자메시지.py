p, w = map(int, input().split())
s = input()

g = {
    "A":(1,1),
    "B":(1,2),
    "C":(1,3),
    "D":(2,1),
    "E":(2,2),
    "F":(2,3),
    "G":(3,1),
    "H":(3,2),
    "I":(3,3),
    "J":(4,1),
    "K":(4,2),
    "L":(4,3),
    "M":(5,1),
    "N":(5,2),
    "O":(5,3),
    "P":(6,1),
    "Q":(6,2),
    "R":(6,3),
    "S":(6,4),
    "T":(7,1),
    "U":(7,2),
    "V":(7,3),
    "W":(8,1),
    "X":(8,2),
    "Y":(8,3),
    "Z":(8,4),
    " ":(0,1)
}

ans = 0
before = -1
for c in s:
    a, b = g[c]
    if a != 0 and before == a :
        ans += w
    before = a
    ans += b*p
print(ans)