n = int(input())
#  (1 ≤ n ≤ 1,000)
dp = [0]*1001

# 3 111 =1 1= o1 1o
# 4 1111 11= 1=1 =11 == 11o 1o1 o11 =o o= oo
# 1 3 5 11 21
# 2+1 6-1 10+1 22-1
# 짝수 ×2+1 홀수 ×2-1

dp[1] = 1
dp[2] = 3
dp[3] = 5

for i in range(4, 1001):
    if i%2 == 0 :
        dp[i] = dp[i-1]*2+1
    else:
        dp[i] = dp[i-1]*2-1

print(dp[n]%10007)
